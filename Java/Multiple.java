public class Multiple {
    public static void main (String[]args){
        int num1 = 10;
        int num2 = 5;

        int multiple = num1 * num2;

        System.out.println("Number 1: " + num1);
        System.out.println("Number 2: " + num2);
        System.out.println("Multiple: " + multiple);
    }
    
}
